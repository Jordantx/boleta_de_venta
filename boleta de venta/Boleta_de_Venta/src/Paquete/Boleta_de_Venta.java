    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        double precio =  Double.parseDouble((txtPrecio.getText() != null && !txtPrecio.getText().trim().equals("")) ? txtPrecio.getText() : "0");
        double descuento = Double.parseDouble((txtDescuento.getText() != null && !txtDescuento.getText().trim().equals("")) ? txtDescuento.getText() : "0");
        int numCantidad = Integer.parseInt(cmbCantidad.getSelectedItem().toString() != null ? cmbCantidad.getSelectedItem().toString(): "0");
        
        double subTotal = numCantidad * precio;
        
        double total = subTotal-descuento;
        
        txtSubTotal.setText(""+subTotal);
        txtTotalPagar.setText(""+total);
        
    }//GEN-LAST:event_jButton1ActionPerformed

